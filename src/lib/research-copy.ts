import type { TopicIdea, WorkflowJob } from "@/types/workflow";

type BuildResearchSummaryInput = {
  seedKeyword: string;
  idea: TopicIdea;
  job: WorkflowJob;
  summaryHooks: string;
};

export function buildLongResearchSummary({
  seedKeyword,
  idea,
  job,
  summaryHooks
}: BuildResearchSummaryInput) {
  const thaiSources = job.research.sources.filter((source) => source.region === "TH");
  const globalSources = job.research.sources.filter((source) => source.region === "Global");
  const sourceHighlights = job.research.sources
    .slice(0, 6)
    .map((source) => `${source.title} จาก ${source.source} ระบุว่า ${source.insight}`)
    .join(" ");
  const gapHighlights = job.research.gaps.join(" ");

  return [
    `หัวข้อรีเสิร์ชนี้โฟกัสที่ "${idea.title}" ซึ่งต่อยอดจาก seed keyword "${seedKeyword}" และมี intent แบบคนที่ต้องการคำตอบจริงมากกว่าบทความนิยามสั้น ๆ เป้าหมายของสรุปนี้คือรวบข้อมูลที่กระจัดกระจายจากหลายแหล่งมาเรียงใหม่ให้พร้อมสำหรับการสร้างบทความ SEO ที่อ่านลื่นและส่งลูกค้าได้จริง.`,
    `วัตถุประสงค์ของรีเสิร์ชรอบนี้คือ ${job.research.objective} กลุ่มผู้อ่านคือ ${job.research.audience} ดังนั้นเนื้อหาที่จะนำไปเขียนต่อไม่ควรเป็นเพียงการรวบ source มาเรียง แต่ต้องช่วยให้ผู้อ่านเข้าใจภาพรวม เห็นเหตุผลเบื้องหลัง และรู้ว่าควรทำอะไรต่อหลังอ่านจบ.`,
    summaryHooks
      ? `คำตอบสั้นจาก search summary และ source snippets ที่ได้มารอบนี้สะท้อนว่า ${summaryHooks}`
      : `แม้ไม่มี summary hook ที่ชัดจาก search engine ในรอบนี้ แต่ภาพรวมจากข้อมูลที่รวบรวมมาชี้ว่าเนื้อหาควรตอบทั้งมุมคำตอบตรง มุมเหตุผล และมุมการใช้งานจริงพร้อมกัน.`,
    thaiSources.length
      ? `ฝั่งข้อมูลไทยช่วยสะท้อน pain point และภาษาคนค้นจริงได้ชัด เช่น ${thaiSources
          .slice(0, 3)
          .map((source) => `${source.title} ที่บอกว่า ${source.insight}`)
          .join(" ")} จุดนี้สำคัญเพราะถ้าบทความตอบด้วยภาษาวิชาการล้วนจะดูไกลตัวและไม่ตอบ intent ของผู้อ่านไทยเท่าที่ควร.`
      : `แม้ข้อมูลไทยจะยังมีไม่มาก แต่บทความควรใช้ภาษาคนอ่านไทยเป็นหลักและแปลความหมายของข้อมูลเชิงเทคนิคให้อ่านง่ายที่สุด.`,
    globalSources.length
      ? `ฝั่งต่างประเทศมีหน้าที่เติมกรอบคิดและมาตรฐานให้เนื้อหาแน่นขึ้น เช่น ${globalSources
          .slice(0, 3)
          .map((source) => `${source.title} ที่ชี้ว่า ${source.insight}`)
          .join(" ")} ส่วนนี้ช่วยให้บทความไม่ยืนอยู่บนความเห็นหรือประสบการณ์ส่วนตัวอย่างเดียว แต่มีหลักฐานรองรับชัดขึ้น.`
      : `ข้อมูลต่างประเทศยังจำเป็นในฐานะแหล่งยืนยันหลักคิด มาตรฐาน และ terminology ที่ยกระดับความน่าเชื่อถือของบทความ.`,
    `เมื่อรวม source ทั้งหมดเข้าด้วยกัน จะเห็น pattern ว่าบทความควรเล่าเป็นลำดับ เริ่มจากคำตอบสั้นและภาพรวมก่อน แล้วค่อยขยายไปยังสิ่งที่ต้องเข้าใจ ข้อมูลจากไทย ข้อมูลจากต่างประเทศ วิธีนำไปใช้จริง และคำถามที่คนค้นต่อจากหัวข้อนี้. ${sourceHighlights}`,
    gapHighlights
      ? `ช่องว่างที่บทความนี้ควรปิดคือ ${gapHighlights} ถ้าปิดจุดเหล่านี้ได้ บทความจะต่างจากคอนเทนต์ทั่วไปที่ตอบแค่ผิวหน้า และจะดูเป็นชิ้นงานที่ผ่านการสรุปใหม่จากข้อมูลจริงมากขึ้น.`
      : `นอกจากการสรุปข้อมูลแล้ว บทความยังต้องมีจุดยืนชัดว่ากำลังช่วยผู้อ่านตัดสินใจเรื่องใด และข้อมูลแต่ละส่วนมีบทบาทอย่างไรต่อการตัดสินใจนั้น.`,
    `ในเชิง editorial งานชิ้นนี้ควรมีโครงแบบงานตัวอย่างจริง คือมี Title, Description, H1, H2, H3, รูปแทรกตาม section และเนื้อหาที่ยาวพอจะอธิบายเหตุผล ไม่ใช่แค่ list bullet สั้น ๆ ภาษาในบทความควรเป็นไทยเป็นหลัก และแทรกชื่อแหล่งอ้างอิงหรือคำเทคนิคอังกฤษเฉพาะจุดที่ช่วยเพิ่มความแม่นยำและความน่าเชื่อถือ.`,
    `สรุปในเชิงการเขียน บทความนี้ควรอ่านแล้วรู้สึกว่า “ผ่านการคิดและรีเสิร์ชมาแล้ว” ไม่ใช่แค่ template เปลี่ยนคำ ดังนั้นทุก section ต้องมีหน้าที่ชัด ทั้งตอบคำถาม ขยายเหตุผล เชื่อมแหล่งข้อมูล และพาคนอ่านไปสู่ next step ที่ใช้งานได้จริง.`
  ].join("\n\n");
}
